function parseTimeToMinutes(value){
  if(!value) return 0;
  const parts = String(value).trim().split(":").map(Number);
  return (parts[0] || 0) * 60 + (parts[1] || 0);
}

function getCurrentMinutes(){
  const now = new Date();
  return now.getHours() * 60 + now.getMinutes();
}

function isVenueOpenByHours(hours){
  if(!hours || !hours.includes("-")) return true;

  const parts = hours.split("-");
  const open = parseTimeToMinutes(parts[0]);
  const close = parseTimeToMinutes(parts[1]);
  const now = getCurrentMinutes();

  if(open === close) return true;

  // Aynı gün kapanıyorsa
  if(open < close){
    return now >= open && now < close;
  }

  // Gece 00:00 sonrası kapanış
  return now >= open || now < close;
}

function getVenueOpenInfo(venue){
  const isOpen = isVenueOpenByHours(venue.hours);
  return {
    isOpen: isOpen,
    text: isOpen ? "● AÇIK" : "● KAPALI",
    className: isOpen ? "open-blink" : "closed-blink"
  };
}

function getCurrentPrice(beer){
  const currentMinutes = getCurrentMinutes();

  if(beer.happyHourPrice && beer.happyHourEnd){
    const endMinutes = parseTimeToMinutes(beer.happyHourEnd);

    if(currentMinutes < endMinutes){
      return beer.happyHourPrice;
    }
  }

  return beer.normalPrice;
}

function isHappyHourActive(beer){
  return Boolean(beer.happyHourPrice && beer.happyHourEnd && getCurrentPrice(beer) === beer.happyHourPrice);
}

function getAllBeersSorted(){
  const list = [];

  venuesData.forEach(venue => {
    venue.beers.forEach(beer => {
      const price = getCurrentPrice(beer);

      list.push({
        type: "beer",
        beer: `${beer.name} ${beer.size}`,
        name: beer.name,
        size: beer.size,
        venue: venue.venue,
        area: venue.area,
        url: venue.url,
        image: venue.cardImage || venue.image,
        price: price,
        normalPrice: beer.normalPrice,
        happyHourPrice: beer.happyHourPrice,
        happyHourEnd: beer.happyHourEnd,
        isCampaign: isHappyHourActive(beer)
      });
    });
  });

  return list.sort((a,b) => a.price - b.price);
}

function getVenuesSorted(){
  return venuesData.map((venue, index) => {
    const beers = venue.beers.map(beer => ({
      ...beer,
      currentPrice: getCurrentPrice(beer)
    }));

    const minPrice = Math.min(...beers.map(beer => beer.currentPrice));

    return {
      ...venue,
      index,
      url: venue.url || ("mekan.html?name=" + encodeURIComponent(venue.venue)),
      minPrice,
      beers
    };
  });
}
